# relational operator(<,>,<=,>=,==,!=)
num1=100
num2=200
print(num1<num2)                #100<200
print(num1<=num2)               #100<=200
print(num1>num2)                #100>200
print(num1>=num2)               #100>=200
print(num1==num2)               #100==200
print(num1!=num2)               #100!=200
